Exception handling is a "safety valve" if you will for when a code cant run
or has an error in some way. it is what the code does when it cant do what its supposed to do.

So if your code is supposed to calculate 2 numbers and it cant (because you entered a string for ex) the exception handling would 
be the code that tells your program to throw the error code...like "sorry you didn't give me two numbers!" or ERROR ERROR!

Just as an example...or log the error or whatever...


